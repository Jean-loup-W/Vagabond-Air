export function afficherEquipe() {
    document.querySelectorAll('.contenu-equipe').forEach(conteneur => {
        const attr = conteneur.dataset.membres;
        const membres = attr
            ? attr.split(',').map(s => equipe[s.trim()]).filter(Boolean)
            : Object.values(equipe);
        const html = membres.map(m =>
            `<p><strong>${m.icone} ${m.nom} (${m.sexe})</strong></p>\n<p>${m.description}</p>`
        ).join('\n\n');
        conteneur.insertAdjacentHTML('beforeend', html);
    });
}

afficherEquipe();
